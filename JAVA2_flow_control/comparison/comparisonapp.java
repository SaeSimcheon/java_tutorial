// 비교연산자
public class comparisonapp{
    public static void main(String[] args){
        System.out.println("1"+"1");
        System.out.println("1" == "1"); // 자바는 문자열 간의 비교연산 안 되나?
        System.out.println(1 == 1);
        System.out.println(1 != 1);
        System.out.println(1 >= 1);
        
    }
}